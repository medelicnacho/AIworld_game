"""STAGE 0 — the voice spike. One question, one number.

Can a spoken line, generated by the real local model and voiced by the real local TTS,
reach the game fast enough to feel like SPEECH rather than a loading screen?

Everything in STAGES.md after this assumes local AI is fast enough to be diegetic. That
assumption has never been tested, and it is the cheapest thing in the project to falsify.

Measures, separately, because the fix differs entirely depending on which dominates:
  - LLM latency (ollama / gemma3:4b) at game-realistic prompt sizes
  - TTS latency (piper), and the REAL-TIME FACTOR (synth seconds per audio second)
  - the total a player would actually wait

    python3 gameworld/bench/voice_spike.py            # full run
    python3 gameworld/bench/voice_spike.py --quick    # 2 samples per case
"""
from __future__ import annotations

import argparse
import statistics
import sys
import time
import wave
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[2] / "localprototype"))

from services.llm import OllamaLLM                      # noqa: E402
from services.tts import PiperTTS, Voice                # noqa: E402

# The two things a companion actually says. A murmur is a fragment; a line is a reply.
CASES = [
    ("murmur", "You are a wandering mind. In one short fragment, under 12 words, murmur "
               "about the ash plains and something you half-remember.", 28),
    ("reply", "You are a companion walking beside someone in a dangerous land. They just "
              "asked: 'what do you make of this place?' Answer in one or two sentences.", 70),
]

VOICE = Voice(model="en_US-lessac-medium.onnx", length_scale=1.0)


def wav_seconds(path: str) -> float:
    with wave.open(path, "rb") as w:
        return w.getnframes() / float(w.getframerate())


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--quick", action="store_true")
    ap.add_argument("--model", default="gemma3:4b")
    args = ap.parse_args()
    n = 2 if args.quick else 4

    llm = OllamaLLM(model=args.model)
    if not llm.available():
        print("FAIL: ollama is not reachable at localhost:11434")
        return 2
    if not PiperTTS.available():
        print("FAIL: no piper voices. Run: bash localprototype/scripts/get_voices.sh")
        return 2

    tts = PiperTTS()
    out = Path(__file__).resolve().parent / "_spike_out"
    out.mkdir(exist_ok=True)

    print(f"model {args.model} · voice {VOICE.model} · {n} samples per case\n")

    # Warm both: first call loads the model into RAM and is not representative of the
    # steady state a running game sees.
    print("warming (model load, not measured) ...", flush=True)
    llm.generate("Say one word.", num_predict=8)
    tts.synth_to("warming up the voice model", VOICE, str(out / "warm.wav"))

    results = {}
    for name, prompt, predict in CASES:
        gen_t, tts_t, audio_s, texts = [], [], [], []
        for i in range(n):
            t0 = time.perf_counter()
            text = llm.generate(prompt, num_predict=predict).strip()
            t1 = time.perf_counter()
            path = str(out / f"{name}_{i}.wav")
            tts.synth_to(text, VOICE, path)
            t2 = time.perf_counter()

            gen_t.append(t1 - t0)
            tts_t.append(t2 - t1)
            audio_s.append(wav_seconds(path))
            texts.append(text)
            print(f"  {name} {i + 1}/{n}  llm {t1 - t0:5.2f}s  tts {t2 - t1:5.2f}s  "
                  f"audio {audio_s[-1]:4.2f}s  |  {text[:64]}")

        med = lambda v: statistics.median(v)   # noqa: E731
        results[name] = {
            "llm": med(gen_t), "tts": med(tts_t), "audio": med(audio_s),
            "total": med(gen_t) + med(tts_t),
            "rtf": med(tts_t) / max(0.01, med(audio_s)),
            "chars": med([len(t) for t in texts]),
        }
        print()

    print("=" * 74)
    print(f"{'case':<8} {'llm':>7} {'tts':>7} {'TOTAL':>8} {'audio':>7} {'rtf':>6}  verdict")
    print("-" * 74)
    ok = True
    for name, r in results.items():
        # A murmur is ambient -- it can be generated ahead of time, so its bar is loose.
        # A REPLY is the one a player waits on, so it carries the real gate.
        bar = 2.5 if name == "reply" else 4.0
        good = r["total"] <= bar
        ok = ok and good
        print(f"{name:<8} {r['llm']:6.2f}s {r['tts']:6.2f}s {r['total']:7.2f}s "
              f"{r['audio']:6.2f}s {r['rtf']:5.2f}x  {'PASS' if good else 'FAIL'} (bar {bar}s)")
    print("=" * 74)

    rtf = max(r["rtf"] for r in results.values())
    print(f"\nTTS real-time factor: {rtf:.2f}x "
          f"({'faster' if rtf < 1 else 'SLOWER'} than realtime)")
    if rtf < 0.35:
        print("  -> piper can also stream: start playing before synthesis finishes,")
        print("     which removes TTS from the wait entirely.")

    print("\nREADING:")
    if ok:
        print("  PASS. Local AI is fast enough to be diegetic. Stage 1 (the bridge) proceeds")
        print("  as written: generate on demand, speak on arrival.")
    else:
        slow = "llm" if results["reply"]["llm"] > results["reply"]["tts"] else "tts"
        print(f"  FAIL, and the {slow.upper()} half dominates. Stage 1 must change shape:")
        if slow == "llm":
            print("   - pre-generate ambient lines during quiet moments and cache them")
            print("   - keep the model tier for DIRECT conversation only; markov for ambient")
            print("   - or fill the wait diegetically (she murmurs while she thinks)")
        else:
            print("   - stream piper output instead of waiting for the whole wav")
            print("   - pre-synth a bank of common ambient lines at load")
    print(f"\nwavs in {out}")
    phase2(args.model, n)
    return 0 if ok else 1


_MAIN = main


# ---------------------------------------------------------------------------------
# PHASE 2 — the mitigations, measured rather than assumed.
#
# Phase 1's verdict: TTS is free (0.05x realtime) and the LLM is the whole wait. Two
# fixes follow from that, and they are cheap to test:
#   (a) SAY LESS. The failing reply generated 70 tokens and produced TEN SECONDS of
#       speech — far too long for a companion line regardless of latency. Fewer tokens
#       is both better writing and a faster response.
#   (b) STREAM. Piper is 20x faster than realtime, so the first sentence can be spoken
#       while the rest is still being generated. What a player waits for is not the whole
#       reply — it is the first sound.
# ---------------------------------------------------------------------------------

import json                       # noqa: E402
import urllib.request             # noqa: E402

SHORT = ("You are a companion walking beside someone in a dangerous land. They asked: "
         "'what do you make of this place?' Answer in ONE short sentence, under 18 words.")


def stream_until_sentence(model, prompt, num_predict, url="http://localhost:11434"):
    """Return (time-to-first-token, time-to-first-complete-sentence, text)."""
    payload = {"model": model, "messages": [{"role": "user", "content": prompt}],
               "stream": True, "keep_alive": "30m",
               "options": {"temperature": 1.0, "num_predict": num_predict, "num_thread": 8}}
    req = urllib.request.Request(f"{url}/api/chat",
                                 data=json.dumps(payload).encode(),
                                 headers={"Content-Type": "application/json"})
    t0 = time.perf_counter()
    first, buf = None, ""
    with urllib.request.urlopen(req, timeout=60) as r:
        for raw in r:
            line = raw.decode("utf-8").strip()
            if not line:
                continue
            d = json.loads(line)
            tok = d.get("message", {}).get("content", "")
            if tok and first is None:
                first = time.perf_counter() - t0
            buf += tok
            if len(buf) > 24 and any(buf.rstrip().endswith(p) for p in ".!?"):
                return first, time.perf_counter() - t0, buf.strip()
            if d.get("done"):
                break
    return first, time.perf_counter() - t0, buf.strip()


def phase2(model="gemma3:4b", n=4):
    tts = PiperTTS()
    out = Path(__file__).resolve().parent / "_spike_out"
    llm = OllamaLLM(model=model)

    print("\n\n" + "=" * 74)
    print("PHASE 2 — mitigations")
    print("=" * 74)

    print("\n(a) SHORT reply, generated whole:")
    tot = []
    for i in range(n):
        t0 = time.perf_counter()
        text = llm.generate(SHORT, num_predict=40).strip()
        t1 = time.perf_counter()
        p = str(out / f"short_{i}.wav")
        tts.synth_to(text, VOICE, p)
        t2 = time.perf_counter()
        tot.append(t2 - t0)
        print(f"  {i + 1}/{n}  total {t2 - t0:5.2f}s  (llm {t1 - t0:4.2f} tts {t2 - t1:4.2f})  "
              f"audio {wav_seconds(p):4.1f}s  |  {text[:58]}")
    short_med = statistics.median(tot)

    print("\n(b) STREAMED — time until the FIRST SENTENCE can start playing:")
    firsts, sents, tta = [], [], []
    for i in range(n):
        ft, st, text = stream_until_sentence(model, SHORT, 60)
        p = str(out / f"stream_{i}.wav")
        t0 = time.perf_counter()
        tts.synth_to(text, VOICE, p)
        synth = time.perf_counter() - t0
        firsts.append(ft or 0)
        sents.append(st)
        tta.append(st + synth)
        print(f"  {i + 1}/{n}  first token {ft:4.2f}s  sentence {st:4.2f}s  "
              f"+tts {synth:4.2f}s  -> AUDIO STARTS {st + synth:4.2f}s  |  {text[:52]}")

    stream_med = statistics.median(tta)

    print("\n" + "-" * 74)
    print(f"  baseline (long reply, whole)      4.68s   FAIL")
    print(f"  (a) short reply, whole            {short_med:4.2f}s   "
          f"{'PASS' if short_med <= 2.5 else 'FAIL'}")
    print(f"  (b) short reply, streamed         {stream_med:4.2f}s   "
          f"{'PASS' if stream_med <= 2.5 else 'FAIL'}  <- time to FIRST SOUND")
    print("-" * 74)
    return short_med, stream_med


